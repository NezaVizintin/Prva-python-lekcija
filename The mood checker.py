mood = input("How do you feel? ")

if mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("You should talk to someone about it.")
elif mood == "excited":
    print("Yay! Glad things are going well!")
elif mood == "relaxed":
    print("Good for you!")
else:
    print("I don't recognise this mood")